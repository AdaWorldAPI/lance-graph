//! Bucket-dispatch Op emitter — **Phase 2** of the Odoo SoA → Foundry SoC
//! pipeline.
//!
//! # Pipeline position
//!
//! ```text
//!   Typed Odoo SoA  (OdooEntity[])
//!        │  style_recipe::derive_style_recipe (Phase 1, PR #433)
//!        ▼
//!   OdooStyleRecipe[]   (DAtom weights + regulatory anchors)
//!        │  THIS MODULE — bucket_corpus + emit_op_dispatch (Phase 2)
//!        ▼
//!   Rust source String  (RECIPE_* consts + per-kind Op structs + static slices)
//!        │  write via build.rs → OUT_DIR → include!()
//!        ▼
//!   Compiled SoC dispatch table  (O(1) recipe lookup at runtime)
//! ```
//!
//! # Conceptual split
//!
//! `derive_style_recipe` (Phase 1) asks: **what does this method DO** — give
//! it a sparse DAtom weight vector.
//!
//! `bucket_corpus` (Phase 2) asks: **what KIND of method is it** — put it in
//! the right dispatch bucket by `OdooMethodKind` semantic role.
//!
//! `emit_op_dispatch` (Phase 2) asks: **what does the compiler need** — emit
//! a deterministic Rust source file the SoC synergy stage compiles into
//! O(1) dispatch tables.
//!
//! # Emitted structure (per bucket that has ≥1 method)
//!
//! ```rust,ignore
//! // --- auto-generated by odoo_blueprint::op_emitter ---
//!
//! // ─── Recipe collapse keys ──────────────────────────────────────────────
//! /// entity=1 compute=4
//! pub const RECIPE_A1B2C3D4: u32 = 0xA1B2C3D4;
//! // ...
//!
//! // ─── Compute bucket (N methods) ────────────────────────────────────────
//! #[derive(Debug, Clone, Copy)]
//! pub struct ComputeOp {
//!     pub method_id: &'static str,
//!     pub recipe_id: u32,
//! }
//! pub static COMPUTE_OPS: &[ComputeOp] = &[
//!     ComputeOp { method_id: "account.move._compute_amount", recipe_id: 0xA1B2C3D4 },
//! ];
//! ```
//!
//! # Determinism guarantees
//!
//! - `RECIPE_*` consts are emitted in ascending `recipe_id` order.
//! - Buckets are emitted in `OdooMethodKind` declaration order.
//! - Within each bucket, `Op` entries are sorted by `recipe_id` then `method_id`.
//!
//! # Zero-dep output
//!
//! The emitted file uses only primitive Rust types (`u32`, `&'static str`).
//! Consumers `include!` it from `OUT_DIR`; no imports needed in the output.

use std::collections::BTreeMap;
use std::fmt::Write as FmtWrite;

use crate::odoo_blueprint::style_recipe::{derive_style_recipe, OdooStyleRecipe};
use crate::odoo_blueprint::{OdooEntity, OdooMethodKind};

