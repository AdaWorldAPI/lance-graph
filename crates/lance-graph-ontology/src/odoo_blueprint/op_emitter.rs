/// One bucketed recipe: method identity + semantic kind + DAtom fingerprint.
///
/// Produced by [`bucket_corpus`] — the bridge between the interpretation step
/// (Phase 1) and the codegen step (Phase 2).
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct BucketedRecipe {
    /// Fully-qualified method id: `"account.move._compute_amount"`.
    pub method_id: String,
    /// Semantic role dispatch bucket — drives Op struct selection.
    pub kind: OdooMethodKind,
    /// DAtom fingerprint + regulatory anchors (the Phase 1 output).
    pub recipe: OdooStyleRecipe,
}

/// Emit statistics produced alongside the Rust source.
#[derive(Debug, Clone)]
pub struct EmitStats {
    /// Total methods processed (one entry per entity×method pair).
    pub total_methods: usize,
    /// Count of unique `recipe_id` values (the collapse count).
    pub unique_recipes: usize,
    /// Per-kind method counts, in `OdooMethodKind` declaration order.
    pub kind_counts: Vec<(OdooMethodKind, usize)>,
}

// ---------------------------------------------------------------------------
// Public functions
// ---------------------------------------------------------------------------

/// Walk every entity's methods, derive their DAtom recipe, and pair each
/// with its semantic method kind.
///
/// Empty entities (no methods) and empty entity slices both produce `vec![]`.
/// All 10 `OdooMethodKind` variants are eligible — including `Helper` and
/// `Override`, whose recipes carry only the `Entity=1` anchor.
pub fn bucket_corpus(entities: &[&OdooEntity]) -> Vec<BucketedRecipe> {
    let mut out = Vec::new();
    for entity in entities {
        for method in entity.methods {
            let recipe = derive_style_recipe(entity, method);
            out.push(BucketedRecipe {
                method_id: recipe.method_id.clone(),
                kind: method.kind,
                recipe,
            });
        }
    }
    out
}

/// Emit the compilable Rust Op dispatch source from a bucketed recipe corpus.
///
/// Returns the source `String` and [`EmitStats`]. Write the source to a file
/// in `OUT_DIR` (e.g. via `build.rs`) and `include!` it in the consumer.
///
/// Calling with an empty `buckets` slice produces a valid Rust file (just
/// the header comments — no structs, no statics). This is intentional: an
/// empty corpus is a valid (though probably misconfigured) input.
pub fn emit_op_dispatch(buckets: &[BucketedRecipe]) -> (String, EmitStats) {
    // Collect unique recipes (BTreeMap gives ascending recipe_id order).
    let mut recipe_map: BTreeMap<u32, &OdooStyleRecipe> = BTreeMap::new();
    for b in buckets {
        recipe_map.entry(b.recipe.recipe_id).or_insert(&b.recipe);
    }

    // Group buckets by kind in declaration order.
    let groups = group_by_kind(buckets);

    // Build kind_counts for stats.
    let kind_counts: Vec<(OdooMethodKind, usize)> =
        groups.iter().map(|(k, v)| (*k, v.len())).collect();

    let mut src = String::new();

    // Header
    writeln!(
        src,
        "// --- auto-generated by odoo_blueprint::op_emitter ---"
    )
    .unwrap();
    writeln!(
        src,
        "// DO NOT EDIT: regenerate via `bucket_corpus` + `emit_op_dispatch`"
    )
    .unwrap();
    writeln!(src, "//").unwrap();
    writeln!(src, "// Buckets : {}", groups.len()).unwrap();
    writeln!(src, "// Unique recipes : {}", recipe_map.len()).unwrap();
    writeln!(src, "// Total methods  : {}", buckets.len()).unwrap();
    writeln!(src).unwrap();

    // 1. Recipe collapse-key consts
    if !recipe_map.is_empty() {
        writeln!(
            src,
            "// ─── Recipe collapse keys \
             ────────────────────────────────────────────────────"
        )
        .unwrap();
        for (id, recipe) in &recipe_map {
            let comment = atom_comment(recipe);
            writeln!(src, "/// {comment}").unwrap();
            writeln!(src, "pub const RECIPE_{id:08X}: u32 = 0x{id:08X};").unwrap();
        }
        writeln!(src).unwrap();
    }

    // 2. Per-kind Op struct + static slice
    for (kind, entries) in &groups {
        let pascal = kind_pascal(*kind);
        let upper = kind_upper(*kind);
        let count = entries.len();

        // Sort for determinism: recipe_id asc, then method_id asc.
        let mut sorted: Vec<&BucketedRecipe> = entries.iter().copied().collect();
        sorted.sort_by(|a, b| {
            a.recipe
                .recipe_id
                .cmp(&b.recipe.recipe_id)
                .then_with(|| a.method_id.cmp(&b.method_id))
        });

        writeln!(
            src,
            "// ─── {pascal} bucket ({count} {}) \
             ─────────────────────────────────────────────────",
            if count == 1 { "method" } else { "methods" }
        )
        .unwrap();
        writeln!(src, "/// An Op in the `{pascal}` dispatch bucket.").unwrap();
        writeln!(src, "#[derive(Debug, Clone, Copy)]").unwrap();
        writeln!(src, "pub struct {pascal}Op {{").unwrap();
        writeln!(src, "    pub method_id: &'static str,").unwrap();
        writeln!(src, "    pub recipe_id: u32,").unwrap();
        writeln!(src, "}}").unwrap();
        writeln!(src).unwrap();
        writeln!(
            src,
            "/// All `{pascal}` Ops, sorted by `recipe_id` then `method_id`."
        )
        .unwrap();
        writeln!(src, "pub static {upper}_OPS: &[{pascal}Op] = &[").unwrap();
        for e in &sorted {
            writeln!(
                src,
                "    {pascal}Op {{ method_id: {:?}, recipe_id: 0x{:08X} }},",
                e.method_id, e.recipe.recipe_id
            )
            .unwrap();
        }
        writeln!(src, "];").unwrap();
        writeln!(src).unwrap();
    }

    let stats = EmitStats {
        total_methods: buckets.len(),
        unique_recipes: recipe_map.len(),
        kind_counts,
    };

    (src, stats)
}

// ---------------------------------------------------------------------------
// Private helpers
