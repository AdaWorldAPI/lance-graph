/// Group `BucketedRecipe` slices by kind, in `OdooMethodKind` declaration
/// order. Kinds with no entries are omitted.
fn group_by_kind(buckets: &[BucketedRecipe]) -> Vec<(OdooMethodKind, Vec<&BucketedRecipe>)> {
    // Accumulate into a BTreeMap keyed by declaration-order index.
    let mut map: BTreeMap<u8, Vec<&BucketedRecipe>> = BTreeMap::new();
    for b in buckets {
        map.entry(kind_ord(b.kind)).or_default().push(b);
    }
    map.into_iter()
        .map(|(ord, v)| (kind_from_ord(ord), v))
        .collect()
}

/// Stable ordinal matching `OdooMethodKind` declaration order.
const fn kind_ord(k: OdooMethodKind) -> u8 {
    match k {
        OdooMethodKind::Compute => 0,
        OdooMethodKind::Inverse => 1,
        OdooMethodKind::Constrain => 2,
        OdooMethodKind::Onchange => 3,
        OdooMethodKind::Action => 4,
        OdooMethodKind::Cron => 5,
        OdooMethodKind::ApiModel => 6,
        OdooMethodKind::ApiModelCreateMulti => 7,
        OdooMethodKind::Override => 8,
        OdooMethodKind::Helper => 9,
    }
}

fn kind_from_ord(ord: u8) -> OdooMethodKind {
    match ord {
        0 => OdooMethodKind::Compute,
        1 => OdooMethodKind::Inverse,
        2 => OdooMethodKind::Constrain,
        3 => OdooMethodKind::Onchange,
        4 => OdooMethodKind::Action,
        5 => OdooMethodKind::Cron,
        6 => OdooMethodKind::ApiModel,
        7 => OdooMethodKind::ApiModelCreateMulti,
        8 => OdooMethodKind::Override,
        9 => OdooMethodKind::Helper,
        _ => unreachable!("kind_ord range is 0..=9"),
    }
}

/// PascalCase name for the emitted Op struct (e.g. `Compute`, `ApiModel`).
const fn kind_pascal(k: OdooMethodKind) -> &'static str {
    match k {
        OdooMethodKind::Compute => "Compute",
        OdooMethodKind::Inverse => "Inverse",
        OdooMethodKind::Constrain => "Constrain",
        OdooMethodKind::Onchange => "Onchange",
        OdooMethodKind::Action => "Action",
        OdooMethodKind::Cron => "Cron",
        OdooMethodKind::ApiModel => "ApiModel",
        OdooMethodKind::ApiModelCreateMulti => "ApiModelCreateMulti",
        OdooMethodKind::Override => "Override",
        OdooMethodKind::Helper => "Helper",
    }
}

/// UPPER_SNAKE name for the emitted static slice (e.g. `COMPUTE`, `API_MODEL`).
const fn kind_upper(k: OdooMethodKind) -> &'static str {
    match k {
        OdooMethodKind::Compute => "COMPUTE",
        OdooMethodKind::Inverse => "INVERSE",
        OdooMethodKind::Constrain => "CONSTRAIN",
        OdooMethodKind::Onchange => "ONCHANGE",
        OdooMethodKind::Action => "ACTION",
        OdooMethodKind::Cron => "CRON",
        OdooMethodKind::ApiModel => "API_MODEL",
        OdooMethodKind::ApiModelCreateMulti => "API_MODEL_CREATE_MULTI",
        OdooMethodKind::Override => "OVERRIDE",
        OdooMethodKind::Helper => "HELPER",
    }
}

/// Human-readable doc-comment line summarising a recipe's atom weights.
///
/// Example: `"entity=1 compute=4 law=8"`.
fn atom_comment(recipe: &OdooStyleRecipe) -> String {
    if recipe.atoms.is_empty() {
        return "empty".to_owned();
    }
    recipe
        .atoms
        .iter()
        .map(|(a, w)| format!("{}={}", a.id(), w))
        .collect::<Vec<_>>()
        .join(" ")
}

